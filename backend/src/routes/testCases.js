const { Router } = require('express');
const { getTestCases, getTestCaseById, createTestCase, updateTestCase, deleteTestCase, updateStepStatus, getProjectTestStats, bulkGenerate, addAttachment, autoGenerateTests, executeTest, retestTest, getAutoGeneratedTests, generateForCompletedSprints } = require('../controllers/testCaseController');
const { auth, authorize } = require('../middleware/auth');
const upload = require('../middleware/upload');
const router = Router();
router.use(auth);
router.get('/', getTestCases);
router.get('/stats/:projectId', getProjectTestStats);
router.get('/auto-generated/:projectId?', authorize('admin', 'project_manager', 'team_lead', 'qa_tester'), getAutoGeneratedTests);
router.get('/:id', getTestCaseById);
router.post('/', authorize('admin', 'project_manager', 'team_lead'), createTestCase);
router.post('/bulk-generate', authorize('admin', 'project_manager', 'team_lead', 'manager', 'qa_tester'), bulkGenerate);
router.post('/auto-generate', authorize('admin', 'project_manager', 'team_lead', 'manager', 'qa_tester'), autoGenerateTests);
router.post('/generate-completed', authorize('admin', 'project_manager', 'team_lead', 'manager', 'qa_tester'), generateForCompletedSprints);
router.post('/:id/execute', authorize('admin', 'project_manager', 'team_lead', 'qa_tester'), executeTest);
router.post('/:id/retest', authorize('admin', 'project_manager', 'team_lead', 'qa_tester'), retestTest);
router.patch('/:id', authorize('admin', 'project_manager', 'team_lead', 'manager', 'qa_tester'), updateTestCase);
router.patch('/:id/step', authorize('admin', 'project_manager', 'team_lead', 'manager', 'qa_tester'), updateStepStatus);
router.post('/:id/attachments', authorize('admin', 'project_manager', 'team_lead', 'manager', 'qa_tester'), upload.single('file'), addAttachment);
router.delete('/:id', authorize('admin', 'project_manager', 'team_lead', 'manager', 'qa_tester'), deleteTestCase);
module.exports = router;